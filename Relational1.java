class Relational1 
{
	public static void main(String[] args) 
	{

		System.out.println(10==10);// true
		System.out.println(10>10.0);//false
		System.out.println(10<=10);//t
		System.out.println(10>=9.999);//true
		System.out.println('a'=='A');//f
		System.out.println('4'==4);//f
		System.out.println(5!= 4.99);//t
		System.out.println(false==true);//f
		//System.out.println(true<=true);//error
		System.out.println(0<0.0);//f
	}
}
